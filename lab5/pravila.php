        </div>
        <div class="col-8 bg-light rounded p-3 ">
            <p class="font-weight-bolder">Правила</br>
                о порядке ведения воинского учета военнообязанных</br>
                и призывников в Республике Казахстан</p>
            <p>Сноска. По всему тексту Правил слова "департаментов (управлений)", "департаментами (управлениями)",
                "Департамент (Управление)" и "Департаментом (Управлением)" заменены соответственно словами "Управлений",
                "Управлениями", "Управление" и "Управлением" - постановлением Правительства РК от 20.03.2008 N 276 .</p>
            <ol>
                <h5>1. Общие положения</h5>
                <li>
                    Правила о порядке ведения воинского учета военнообязанных и призывников в Республике Казахстан
                    (далее - Правила) регулируют порядок ведения воинского учета военнообязанных и призывников в
                    Республике Казахстан.
                </li>
                <li>
                    Воинский учет граждан Республики Казахстан организуется в соответствии с законами Республики
                    Казахстан от 16 июня 1997 года "О мобилизационной подготовке и мобилизации в Республике Казахстан",
                    от 7 января 2005 года "Об обороне и Вооруженных Силах Республики Казахстан", от 8 июля 2005 года "О
                    воинской обязанности и воинской службе" и настоящими Правилами.
                </li>
                <li>Воинский учет осуществляется постоянно и охватывает все категории граждан, подлежащих воинскому
                    учету, за исключением:
                    <ol>
                        <li>женщин, не имеющих военно-учетной специальности;</li>
                        <li> освобожденных от исполнения воинской обязанности;</li>
                        <li>отбывающих наказание в виде лишения свободы;</li>
                        <li>постоянно проживающих за пределами Республики Казахстан.</li>
                    </ol>
                </li>
                <li>Целью воинского учета является определение количественного и качественного состава призывных и
                    мобилизационных людских ресурсов в интересах их эффективного использования для обеспечения обороны
                    страны и безопасности государства.
                </li>
                <li> Основными задачами воинского учета являются:
                    <ol>
                        <li>проведение плановой работы по подготовке необходимых воинских специалистов из числа граждан,
                            пребывающих в запасе, для обеспечения мероприятий по планомерному переводу Вооруженных Сил
                            Республики Казахстан, других войск и воинских формирований, государственных органов с
                            мирного на военное положение и последующего их доукомплектования личным составом;
                        </li>
                        <li>своевременное выявление и определение количественного и качественного состава имеющихся в
                            Республике призывных ресурсов в интересах рационального распределения их по видам, родам
                            войск Вооруженных Сил для обеспечения организованного проведения очередных призывов граждан
                            на срочную воинскую службу;
                        </li>
                        <li>осуществление контроля за выполнением гражданами Республики Казахстан Закона Республики
                            Казахстан "О воинской обязанности и воинской службе" и настоящих Правил.
                        </li>
                    </ol>
                </li>
                <li> Продолжительность нахождения граждан на воинском учете определяется в соответствии с Законом
                    Республики Казахстан "О воинской обязанности и воинской службе".
                </li>
                <li>Воинский учет подразделяется на общий и специальный.
                    <p>На общем воинском учете состоят граждане, которые не забронированы за государственными органами
                        или организациями на период мобилизации и на военное время.</p>
                    <p>На специальном воинском учете состоят граждане, которые в установленном порядке бронируются за
                        органами или организациями на период мобилизации и на военное время.</p>
                    <p>Воинский учет граждан Республики Казахстан, временно проживающих за границей, осуществляется в
                        порядке, установленном настоящими Правилами, по прибытии их на постоянное место жительства в
                        Республику Казахстан.</p>
                </li>
                <li>Воинский учет граждан осуществляется местными органами военного управления (далее - MOBУ) по месту
                    жительства.
                    <p>В населенных пунктах, где отсутствуют МОВУ, воинский учет граждан осуществляется акимами
                        поселков, аулов (сел), аульного (сельского) округа (далее - акимы) в целях создания наибольших
                        удобств военнообязанным при оформлении приема и снятия с учета непосредственно по месту
                        жительства и обеспечения реальности персонального (качественного) учета всех военнообязанных и
                        призывников.</p>
                    <p> Государственные органы и организации, независимо от их ведомственной подчиненности и формы
                        собственности (далее - организации) ведут воинский учет граждан при наличии работающих
                        военнообязанных и призывников, в соответствии с настоящими Правилами.</p>
                    <p> Общее руководство и контроль работы по организации, ведению воинского учета граждан
                        осуществляется МОВУ (города, района).</p>
                    <p>По требованию МОВУ (города, района) организации представляют документацию, подтверждающую
                        количественный состав работающих военнообязанных и призывников.</p>
                </li>
                <li> Первоначальная постановка граждан мужского пола на воинский учет в МОВУ (города, района)
                    осуществляется ежегодно с января по март комиссией по приписке граждан к призывному участку, а
                    граждан, получивших гражданство Республики Казахстан, в течение всего календарного года.
                </li>
                <p>Первоначальная постановка на воинский учет граждан женского пола осуществляется после получения ими
                    военно-учетной специальности и достижения 18 лет.</p>
            </ol>

